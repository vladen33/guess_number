# Создаём переменную со значением типа string.
item = 'Это строка'
# Выводим тип переменной.
print(type(item))
# В терминал будет выведено: <class 'str'>

# Присваиваем переменной новое значение типа integer.
item = 1234
# Выводим тип переменной.
print(type(item))
# В терминал будет выведено: <class 'int'>
